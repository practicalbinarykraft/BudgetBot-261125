/**
 * Режимы расчёта капитала на графике
 */
export type CapitalMode = 'cash' | 'networth';

/**
 * Режим "cash" - Денежный капитал
 * Показывает только деньги + доходы - расходы
 * НЕ включает стоимость имущества и долгов
 */

/**
 * Режим "networth" - Полный капитал
 * Показывает деньги + стоимость имущества - долги + доходы - расходы
 * Включает всё: и деньги, и активы, и обязательства
 */

export interface CapitalModeConfig {
  mode: CapitalMode;
  includeAssets: boolean;  // Включать стоимость активов в капитал
  includeLiabilities: boolean;  // Вычитать стоимость долгов из капитала
}
